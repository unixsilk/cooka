let currentThemeIndex = 0;

function getLangFromUrl() {
  const params = new URLSearchParams(window.location.search);
  const lang = params.get("lang");
  return lang === "tr" || lang === "de" || lang === "sq" ? lang : null;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem("cooka_lang", lang);
  document.documentElement.lang = lang;
}

function applyTheme(theme) {
  const root = document.documentElement;
  Object.entries(theme.vars).forEach(([key, value]) => {
    root.style.setProperty(key, value);
  });
  localStorage.setItem("cooka_theme", theme.id);
  const btn = document.getElementById("theme-toggle");
  if (btn) {
    btn.title = theme.label;
  }
}

function initTheme() {
  const stored = localStorage.getItem("cooka_theme");
  if (stored) {
    const idx = THEMES.findIndex((t) => t.id === stored);
    if (idx >= 0) currentThemeIndex = idx;
  }
  applyTheme(THEMES[currentThemeIndex]);

  const btn = document.getElementById("theme-toggle");
  if (btn) {
    btn.addEventListener("click", () => {
      currentThemeIndex = (currentThemeIndex + 1) % THEMES.length;
      applyTheme(THEMES[currentThemeIndex]);
    });
  }
}

function initLang() {
  const fromUrl = getLangFromUrl();
  const stored = localStorage.getItem("cooka_lang");
  const lang = fromUrl || stored || "de";
  setLang(lang);

  const select = document.getElementById("lang-select");
  if (select) {
    select.value = lang;
    select.addEventListener("change", () => {
      const next = select.value;
      setLang(next);
      updateLinks();
      applyI18n();
      initPage();
    });
  }
}

function t(key) {
  return I18N[currentLang][key] || key;
}

function applyI18n() {
  const page = document.body.getAttribute("data-page");
  if (page && I18N[currentLang].titles[page]) {
    document.title = I18N[currentLang].titles[page];
  }

  const nodes = document.querySelectorAll("[data-i18n]");
  nodes.forEach((node) => {
    const key = node.getAttribute("data-i18n");
    node.textContent = t(key);
  });
}

function updateLinks() {
  const links = document.querySelectorAll("[data-link]");
  links.forEach((link) => {
    const base = link.getAttribute("data-link");
    if (base) {
      link.href = `${base}?lang=${currentLang}`;
    }
  });
}

function getCommentKey(recipeId) {
  return `cooka_comments_${recipeId}`;
}

function loadComments(recipeId) {
  const raw = localStorage.getItem(getCommentKey(recipeId));
  if (!raw) return [];
  try {
    const data = JSON.parse(raw);
    return Array.isArray(data) ? data : [];
  } catch {
    return [];
  }
}

function saveComments(recipeId, comments) {
  localStorage.setItem(getCommentKey(recipeId), JSON.stringify(comments));
}

function renderComments(recipeId) {
  const list = document.getElementById("comment-list");
  if (!list) return;

  const comments = loadComments(recipeId);
  list.innerHTML = "";

  if (comments.length === 0) {
    const empty = document.createElement("p");
    empty.className = "muted";
    empty.textContent = t("comment_empty");
    list.appendChild(empty);
    return;
  }

  comments.forEach((c) => {
    const item = document.createElement("div");
    item.className = "comment-item";

    const head = document.createElement("div");
    head.className = "comment-head";
    head.textContent = `${c.name} · ${c.rating}/5`;
    item.appendChild(head);

    const body = document.createElement("p");
    body.textContent = c.text;
    item.appendChild(body);

    list.appendChild(item);
  });
}

function setupCommentForm(recipeId) {
  const form = document.getElementById("comment-form");
  if (!form) return;

  form.addEventListener("submit", (e) => {
    e.preventDefault();
    const name = document.getElementById("comment-name").value.trim();
    const rating = parseInt(document.getElementById("comment-rating").value, 10);
    const text = document.getElementById("comment-text").value.trim();
    if (!name || !text) return;

    const comments = loadComments(recipeId);
    comments.unshift({ name, rating, text, ts: Date.now() });
    saveComments(recipeId, comments);
    form.reset();
    renderComments(recipeId);
  });
}

function localizeField(field) {
  if (!field) return "";
  if (typeof field === "string") return field;
  if (field[currentLang]) return field[currentLang];
  return field.de || field.tr || "";
}

function formatRating(value) {
  if (typeof value !== "number") return "";
  return value.toFixed(1);
}

