function renderCategories(categories) {
  if (!Array.isArray(categories) || categories.length === 0) return null;
  const wrap = document.createElement("div");
  wrap.className = "tags";
  categories.forEach((cat) => {
    const tag = document.createElement("span");
    tag.className = "tag";
    tag.textContent = labelCategory(cat);
    wrap.appendChild(tag);
  });
  return wrap;
}

function parseQuantityPrefix(text) {
  const cleaned = text.replace(",", ".");
  const match = cleaned.match(/^(\d+(?:\.\d+)?|\d+\/\d+)\s+(.*)$/);
  if (!match) return null;

  let value = 0;
  if (match[1].includes("/")) {
    const [a, b] = match[1].split("/");
    value = parseFloat(a) / parseFloat(b);
  } else {
    value = parseFloat(match[1]);
  }
  if (Number.isNaN(value)) return null;
  return { value, rest: match[2], originalSep: text.includes(",") ? "," : "." };
}

function formatQuantity(value, originalSep) {
  const rounded = Math.round(value * 10) / 10;
  let out = rounded % 1 === 0 ? String(Math.round(rounded)) : String(rounded);
  if (originalSep === ",") out = out.replace(".", ",");
  return out;
}

function scaleIngredient(text, factor) {
  const parsed = parseQuantityPrefix(text);
  if (!parsed) return text;
  const nextValue = parsed.value * factor;
  return `${formatQuantity(nextValue, parsed.originalSep)} ${parsed.rest}`;
}

async function loadRecipes() {
  const note = document.getElementById("data-note");
  const client = typeof getSupabaseClient === "function" ? getSupabaseClient() : null;

  if (!client) {
    if (note) note.textContent = t("data_demo");
    return typeof DEMO_RECIPES !== "undefined" ? DEMO_RECIPES : [];
  }

  const { data, error } = await client.from("recipes").select("*").order("title");
  if (error || !data) {
    if (note) note.textContent = t("data_error");
    return typeof DEMO_RECIPES !== "undefined" ? DEMO_RECIPES : [];
  }

  if (note) note.textContent = t("data_supabase");
  return data;
}

function renderRecipeList(recipes) {
  const list = document.getElementById("recipe-list");
  if (!list) return;

  list.innerHTML = "";
  recipes.forEach((recipe) => {
    const card = document.createElement("div");
    card.className = "card recipe-card";
    const href = `recipe.html?id=${encodeURIComponent(recipe.id)}&lang=${currentLang}`;
    card.setAttribute("role", "link");
    card.setAttribute("tabindex", "0");
    card.addEventListener("click", () => {
      window.location.href = href;
    });
    card.addEventListener("keydown", (e) => {
      if (e.key === "Enter" || e.key === " ") {
        e.preventDefault();
        window.location.href = href;
      }
    });

    if (recipe.image_url) {
      const wrap = document.createElement("div");
      wrap.className = "image-ambilight";
      wrap.style.setProperty("--glow-url", `url("${recipe.image_url}")`);
      const img = document.createElement("img");
      img.src = recipe.image_url;
      img.alt = localizeField(recipe.title);
      wrap.appendChild(img);
      card.appendChild(wrap);
    }

    const title = document.createElement("h2");
    title.textContent = localizeField(recipe.title);
    card.appendChild(title);

    const summary = document.createElement("p");
    summary.textContent = localizeField(recipe.summary);
    card.appendChild(summary);

    const meta = document.createElement("p");
    meta.className = "muted";
    meta.textContent = recipe.time_min
      ? `${t("time_prefix")}${recipe.time_min}${t("time_suffix")}`
      : "";
    card.appendChild(meta);

    if (recipe.persons || recipe.rating) {
      const sub = document.createElement("p");
      sub.className = "muted";
      const persons = recipe.persons ? `${t("persons_label")}: ${recipe.persons}` : "";
      const rating = recipe.rating ? `${t("rating_label")}: ${formatRating(recipe.rating)}` : "";
      sub.textContent = [persons, rating].filter(Boolean).join(" · ");
      card.appendChild(sub);
    }

    const tags = renderCategories(recipe.categories);
    if (tags) card.appendChild(tags);

    list.appendChild(card);
  });
}

